SELECT
ms.SHOP_ID AS shopId
,ms.SHOP_NAME AS shopName
,ms.CATEGORY_ID AS categoryId
,mc.CATEGORY_NAME AS categoryName
FROM m_shop AS ms
INNER JOIN m_category AS mc ON ms.CATEGORY_ID=mc.CATEGORY_ID
WHERE ms.CATEGORY_ID=1
AND ms.DEL_KBN=0
AND mc.DEL_KBN=0
ORDER BY ms.SHOP_ID ASC,ms.SHOP_NAME ASC;

SELECT
    mg.GOOD_ID
     ,mg.GOOD_NAME
     ,mg.PRICE
     ,mo.COUNTRY_ID
     ,mc.COUNTRY_NAME
     ,mo.ORIGIN_ID
     ,ORIGIN_NAME
FROM m_good AS mg
INNER JOIN m_origin AS mo ON mg.ORIGIN_ID=mo.COUNTRY_ID
INNER JOIN m_country AS mc ON mo.COUNTRY_ID=mc.COUNTRY_ID
WHERE SHOP_ID=4;



